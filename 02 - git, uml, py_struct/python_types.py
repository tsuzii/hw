# 1. Типы данных

# Целые числа - int
number = 1
print(number)
print(2)
print(number, 2)  # вывести на консоль несколько значений через пробел

# Логические - bool - True, False
print(1 < 2)
print(10 < 2)

# Вещественные числа - float
print(10.6546754)
print(10 / 3)

# Строки - str
print("Hello!")
print('Hello!')
print('He say "Hello!"')

# 2. Преобразрвание типов
num1 = "5"
num2 = "19"
print(num1 + num2)
print(int(num1) + int(num2))
print(float(10))
print(int(19.3421))
